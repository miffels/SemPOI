package de.unima.sempoi.server.model;

import java.net.URL;

public class Picture {

	private String title;
	private URL url;
	
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public URL getUrl() {
		return url;
	}
	public void setUrl(URL url) {
		this.url = url;
	}
}
